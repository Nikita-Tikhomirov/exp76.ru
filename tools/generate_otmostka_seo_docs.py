def ensure_dirs() -> None:
    for subfolder in SUBFOLDERS:
        (ROOT / subfolder).mkdir(parents=True, exist_ok=True)


def create_doc() -> Document:
    doc = Document()
    style = doc.styles["Normal"]
    style.font.name = "Calibri"
    style.font.size = Pt(11)
    return doc


def add_kv(doc: Document, key: str, value: str) -> None:
    paragraph = doc.add_paragraph()
    paragraph.add_run(f"{key}: ").bold = True
    paragraph.add_run(value)


def add_bullets(doc: Document, items: list[str]) -> None:
    for item in items:
        doc.add_paragraph(item, style="List Bullet")


def add_rows(doc: Document, rows: list[str]) -> None:
    for row in rows:
        doc.add_paragraph(row)


def add_faq(doc: Document, faq_items: list[tuple[str, str]]) -> None:
    for question, answer in faq_items:
        doc.add_paragraph(question, style="List Bullet")
        doc.add_paragraph(answer)


def write_core_doc(path: Path, page: dict) -> None:
    doc = create_doc()
    doc.add_heading(page["name"], level=1)
    add_kv(doc, "URL", page["url"])
    add_kv(doc, "Slug", page["slug"])
    add_kv(doc, "Шаблон", "category-87.php")
    add_kv(doc, "Интент", page["intent"])

    doc.add_heading("SEO-мета", level=2)
    add_kv(doc, "Title", page["title"])
    add_kv(doc, "H1", page["h1"])
    add_kv(doc, "Description", page["description"])

    doc.add_heading("Кластер запросов", level=2)
    add_bullets(doc, page["queries"])

